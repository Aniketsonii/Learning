package Learning.Java.Basics;
public class strings {
    public static void main(String[] args) {
        String str1 = "Hello";
        System.out.println(str1); // Hello
        System.out.println(str1.length()); // 5
        System.out.println(str1.toLowerCase());
        System.out.println(str1.toUpperCase());
        System.out.println(str1.indexOf("e"));
        System.out.println(str1 + "yellamma");
        System.out.println(str1.concat(" World"));
        System.out.println("10"+"20");
        System.out.println(10+20);

        System.out.println("aniket \"are oo baba\"");
        System.out.println("aniket \'are oo baba\'");
        System.out.println("aniket \\are oo baba\\");


        System.out.println("aniket \nare oo baba\\");
        System.out.println("aniket \rare oo baba\\");
        System.out.println("aniket \tare oo baba\\");
        System.out.println("aniket \bare oo baba\\");
        System.out.println("aniket \fare oo baba\\");
        
    }
}
